package com.itwill.giga_box.book;

import java.sql.Date;

import com.itwill.giga_box.member.Member;
import com.itwill.giga_box.movie_schedule.Movie_schedule;
import com.itwill.giga_box.multiplex.Multiplex;

public class Book {
	private int book_no; //예매 번호
	private Date book_date; //예매한 날짜
	private int book_seat_count; //좌석수
	private String book_seat_no; //좌석번호
	private String m_id; //회원 아이디
	private int ms_no; //상영 번호
		
	private Member member;
	private Multiplex multiplex;
	private Movie_schedule movie_schedule;
	
	public Book() {
	}

	public Book(int book_no, Date book_date, int book_seat_count, String book_seat_no, String m_id, int ms_no) {
		super();
		this.book_no = book_no;
		this.book_date = book_date;
		this.book_seat_count = book_seat_count;
		this.book_seat_no = book_seat_no;
		this.m_id = m_id;
		this.ms_no = ms_no;
	}

	public int getBook_no() {
		return book_no;
	}

	public void setBook_no(int book_no) {
		this.book_no = book_no;
	}

	public Date getBook_date() {
		return book_date;
	}

	public void setBook_date(Date book_date) {
		this.book_date = book_date;
	}

	public int getBook_seat_count() {
		return book_seat_count;
	}

	public void setBook_seat_count(int book_seat_count) {
		this.book_seat_count = book_seat_count;
	}

	public String getBook_seat_no() {
		return book_seat_no;
	}

	public void setBook_seat_no(String book_seat_no) {
		this.book_seat_no = book_seat_no;
	}

	public String getM_id() {
		return m_id;
	}

	public void setM_id(String m_id) {
		this.m_id = m_id;
	}

	public int getMs_no() {
		return ms_no;
	}

	public void setMs_no(int ms_no) {
		this.ms_no = ms_no;
	}

	@Override
	public String toString() {
		return "Book [book_no=" + book_no + ", book_date=" + book_date + ", book_seat_count=" + book_seat_count
				+ ", book_seat_no=" + book_seat_no + ", m_id=" + m_id + ", ms_no=" + ms_no + ", member=" + member
				+ ", multiplex=" + multiplex + ", movie_schedule=" + movie_schedule + "]";
	}
	

	
	

	


	
	
	
	
}
