package com.itwill.giga_box.event;

public class Event {
	private int event_no; //이벤트 번호
	private String event_name; //이벤트 명
	private String event_path; //이벤트 경로
	private String event_date; //이벤트 기간
	private String m_id; //회원 아이디
	private String event_path2;  //이벤트 뒷면 경로
	
	public Event() {
	}
	
	
	

	public Event(int event_no, String event_name, String event_path, String event_date, String m_id,
			String event_path2) {
		super();
		this.event_no = event_no;
		this.event_name = event_name;
		this.event_path = event_path;
		this.event_date = event_date;
		this.m_id = m_id;
		this.event_path2 = event_path2;
	}




	public String getEvent_path2() {
		return event_path2;
	}




	public void setEvent_path2(String event_path2) {
		this.event_path2 = event_path2;
	}




	public int getEvent_no() {
		return event_no;
	}
	public void setEvent_no(int event_no) {
		this.event_no = event_no;
	}
	public String getEvent_name() {
		return event_name;
	}
	public void setEvent_name(String event_name) {
		this.event_name = event_name;
	}
	public String getEvent_path() {
		return event_path;
	}
	public void setEvent_path(String event_path) {
		this.event_path = event_path;
	}
	public String getEvent_date() {
		return event_date;
	}
	public void setEvent_date(String event_date) {
		this.event_date = event_date;
	}
	public String getM_id() {
		return m_id;
	}
	public void setM_id(String m_id) {
		this.m_id = m_id;
	}




	@Override
	public String toString() {
		return "Event [event_no=" + event_no + ", event_name=" + event_name + ", event_path=" + event_path
				+ ", event_date=" + event_date + ", m_id=" + m_id + ", event_path2=" + event_path2 + "]";
	}



	
	
}
